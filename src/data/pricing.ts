/** Сопоставить текст кнопки или ручной ввод с разделом прайса. */
export function resolvePricingKey(raw: string): PricingSectionKey | undefined {
  const text = raw.trim().toLowerCase();
  if (!text) return undefined;

  // Точное совпадение с подписью кнопки.
  const byLabel = pricingMenu.find((m) => m.label.toLowerCase() === text);
  if (byLabel) return byLabel.key;

  // Ввод числом 1–15.
  const n = Number(text);
  if (Number.isInteger(n) && n >= 1 && n <= pricingMenu.length) {
    return pricingMenu[n - 1].key;
  }

  // Ключевые слова.
  for (const [re, key] of PRICING_KEYWORDS) {
    if (re.test(text)) return key;
  }
  return undefined;
}
