export type PricingNav = "back" | "menu";

/** Распознать навигацию: «назад к прайсу» или «главное меню». */
export function resolveNav(raw: string): PricingNav | undefined {
  const t = raw.trim().toLowerCase();
  if (t === BACK_BUTTON.toLowerCase()) return "back";
  if (t === MAIN_MENU_BUTTON.toLowerCase()) return "menu";
  if (/^(назад|back|прайс|price)$/.test(t)) return "back";
  if (/^(меню|главное меню|help|помощь)$/.test(t)) return "menu";
  return undefined;
}

// Ручной ввод: подстроки → раздел. Порядок важен (более частное раньше общего).
